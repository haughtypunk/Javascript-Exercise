// Grade Problem

// student marks :

// 10 -Amazing
// 5 - Good
// 3 - Poor
// 0 - Fail

var marks = 10;

if(marks === 10){
    console.log('Amazing')
} else if(marks === 5){
    console.log('Good')
} else if(marks === 3){
    console.log('Poor')
} else if(marks === 0){
    console.log('Fail')
} else {
    console.log('Blah!!')
}